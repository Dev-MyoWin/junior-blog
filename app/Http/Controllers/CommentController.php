     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)

    {
        $this->validate($request, [
            
            "name" => 'required',
            "email" => "required",
            "description" => "required",
        ],
            [
                
                'title.required' => 'Enter your name',
                'email.required' => 'Enter your email',
                'description.required' => 'Enter your message',
                
            ]
        );
       
       
        Comment::create(['name'=>$request->name,'email'=>$request->email,'description'=>$request->description,'post_slug'=>$request->slug,'post_id'=>$request->id]);
        
         return redirect()->route('post',$request->slug);
    }

